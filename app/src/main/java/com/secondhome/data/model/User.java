package com.secondhome.data.model;

public class User {
    String first_name;
    String last_name;
    String user_email;
    String user_type;
    int nr_owned_pets;

    public String getFirst_name() {
        return first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public String getUser_email() {
        return user_email;
    }

    public String getUser_type() {
        return user_type;
    }

    public int getNr_owned_pets() {
        return nr_owned_pets;
    }



}
